$(function() {
    $('.slider').jcarousel()
        .jcarousel({
            wrap: 'circular'
        })
        .jcarouselAutoscroll({
            interval: 4000
        });

    $('.slider_pagination')
        .on('jcarouselpagination:active', 'a', function() {
            $(this).addClass('active');
        })
        .on('jcarouselpagination:inactive', 'a', function() {
            $(this).removeClass('active');
        })
        .on('click', function(e) {
            e.preventDefault();
        })
        .jcarouselPagination({
            perPage: 1,
            item: function(page) {
                return '<a href="#' + page + '">' + page + '</a>';
            }
        });
});